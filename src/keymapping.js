export const mapping = (octaveOffset = 0) => {
  const keys = [
    'Z',
    'S',
    'X',
    'D',
    'C',
    'V',
    'G',
    'B',
    'H',
    'N',
    'J',
    'M',
    ',',
    'L',
    '.',
    ';',
    '/',
    'Q',
    '2',
    'W',
    '3',
    'E',
    'R',
    '5',
    'T',
    '6',
    'Y',
    '7',
    'U',
    'I',
    '9',
    'O',
    '0',
    'P',
    '[',
    '=',
    ']'
  ];
  const notes = [
    'C4',
    'C#4',
    'D4',
    'D#4',
    'E4',
    'F4',
    'F#4',
    'G4',
    'G#4',
    'A4',
    'A#4',
    'B4',
    'C5',
    'C#5',
    'D5',
    'D#5',
    'E5',
    'C5',
    'C#5',
    'D5',
    'D#5',
    'E5',
    'F5',
    'F#5',
    'G5',
    'G#5',
    'A5',
    'A#5',
    'B5',
    'C6',
    'C#6',
    'D6',
    'D#6',
    'E6',
    'F6',
    'F#6',
    'G6'
  ];
  const mapping = {};
  for (let i = 0; i < notes.length; i++) {
    const note = notes[i];
    const octave = Number(note[note.length - 1]) + octaveOffset;
    notes[i] = note.substring(0, note.length - 1) + octave;
    mapping[keys[i]] = notes[i];
  }
  return mapping;
};
